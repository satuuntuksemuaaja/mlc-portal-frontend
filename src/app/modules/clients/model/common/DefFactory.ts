  static getDefinitions(level1: string, level2: string, level3: string) {
    const all = DefFactory.loadAll();
    const res = [];

    for (let index = 0; index < all.length; index++) {
      const element = all[index];
      if (
        element.classifications[0] === level1 &&
        element.classifications[1] === level2 &&
        element.classifications[2] === level3
      ) {
        res.push(element);
      }
    }
    return res;
  }

